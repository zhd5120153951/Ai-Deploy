def overlap(box1,box2): # 是否有交集
    minx1, miny1, maxx1, maxy1 = box1[0:4]
    minx2, miny2, maxx2, maxy2 = box2[0:4]
    minx = max(minx1, minx2)
    miny = max(miny1, miny2)
    maxx = min(maxx1, maxx2)
    maxy = min(maxy1, maxy2)
    if minx > maxx or miny > maxy:
        return 0
    else:
        return 1


